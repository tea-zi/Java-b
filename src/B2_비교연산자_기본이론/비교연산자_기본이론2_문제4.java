package B2_비교연산자_기본이론;

public class 비교연산자_기본이론2_문제4 {
	public static void main(String[] args) {		
		

		
		double a = 0;
		/*
		 * 0.9-a = 0.5+0.2
		 * 0.9-a = 0.7
		 * a = 0.9-0.7
		 * a = 0.2
		 */
		a = 0.2;
		System.out.println(0.3  * 3 - a == 0.1 * 5 + 0.2);
		
		double b = 0;
		/*
		 * 1.5-b = 7
		 * b = -7+1.5
		 * b = -5.5
		 */
		b = -5.5;
		System.out.println(0.5 * 3 - b == 7);
	}
}